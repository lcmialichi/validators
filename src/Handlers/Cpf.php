<?php

namespace Validators\Handlers;

class Cpf implements \Validators\Contracts\ValidatorHandler
{
    public function handle($cpf): bool
    {
        if(!is_string($cpf) && !is_numeric($cpf)){
            return false;
        }

        if (strlen($cpf) != 11 || preg_match('/(\d)\1{10}/', $cpf)) {
            return false;
        }

        for ($t = 9; $t < 11; $t++) {
            for ($d = 0, $c = 0; $c < $t; $c++) {
                $d += $cpf[$c] * (($t + 1) - $c);
            }
            $d = ((10 * $d) % 11) % 10;
            if ($cpf[$c] != $d) {
                return false;
            }
        }
        return true;
    }
}
